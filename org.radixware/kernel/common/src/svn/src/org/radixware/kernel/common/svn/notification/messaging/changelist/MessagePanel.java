    private final DocumentListener listener = new DocumentListener() {

        @Override
        public void insertUpdate(DocumentEvent e) {
            saveNotes();
        }

        @Override
        public void removeUpdate(DocumentEvent e) {
            saveNotes();
        }

        @Override
        public void changedUpdate(DocumentEvent e) {
            saveNotes();
        }
    };

    private void saveNotes() {
        element.setMessage(lang, edMessage.getText());

    }
    ChangeListEditorPanel.MessageElement element;

    void open(ChangeListEditorPanel.MessageElement e) {
        this.element = e;
        edMessage.getDocument().removeDocumentListener(listener);
        String text = element.getMessage(lang);
        if (text == null) {
            text = "";
        }
        edMessage.setText(text);
        edMessage.getDocument().addDocumentListener(listener);
    }
