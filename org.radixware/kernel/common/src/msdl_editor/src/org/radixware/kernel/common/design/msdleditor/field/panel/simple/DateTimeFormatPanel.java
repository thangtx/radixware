    private EDateTimeFormat parentValue;
    public static class DateTimeModel extends DefaultComboBoxModel {
        public DateTimeModel() {
            super(new EDateTimeFormat[]{EDateTimeFormat.NOTDEFINED,
                                  EDateTimeFormat.FLORA,
                                  EDateTimeFormat.JAVA,
                                  EDateTimeFormat.STR});
        }
    }

