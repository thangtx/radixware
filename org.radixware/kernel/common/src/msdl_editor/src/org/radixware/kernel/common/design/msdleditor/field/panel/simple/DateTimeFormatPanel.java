    public void setModel(ComboBoxModel model) {
        jComboBoxUnit.setModel(model);
    }

    public void setFormat(EDateTimeFormat value, EDateTimeFormat parentValue) {
        this.parentValue = parentValue;
        getSetParentPanel().setSelected(false);
        jComboBoxUnit.getModel().setSelectedItem(value);
        getSetParentPanel().setEnabled(parentValue != EDateTimeFormat.NOTDEFINED);
        if (value == EDateTimeFormat.NOTDEFINED) {
            getSetParentPanel().setSelected(parentValue != EDateTimeFormat.NOTDEFINED);
            jComboBoxUnit.getModel().setSelectedItem(parentValue);
            jComboBoxUnit.setEnabled(parentValue == EDateTimeFormat.NOTDEFINED);
        }
    }

    public EDateTimeFormat getComboBoxValue() {
        return (EDateTimeFormat)jComboBoxUnit.getModel().getSelectedItem();
    }

    public EDateTimeFormat getFormat() {
        if (getSetParentPanel().getSelected()) {
            jComboBoxUnit.getModel().setSelectedItem(parentValue);
            jComboBoxUnit.setEnabled(false);
            return EDateTimeFormat.NOTDEFINED;
        }
        else {
            jComboBoxUnit.setEnabled(true);
            EDateTimeFormat result = (EDateTimeFormat)jComboBoxUnit.getModel().getSelectedItem();
            if (result == EDateTimeFormat.NOTDEFINED && parentValue != EDateTimeFormat.NOTDEFINED) {
                jComboBoxUnit.getModel().setSelectedItem(parentValue);
                jComboBoxUnit.setEnabled(false);
                getSetParentPanel().setSelected(true);
            }
            return result;
        }
    }

    public SetParentPanel getSetParentPanel() {
        return setParentPanel;
    }

