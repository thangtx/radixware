    public void open(StructureFieldModel field) {
        super.open(field.getMsdlField());
        opened = false;
        this.field = field;
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        model.addElement(EEncoding.BIN);
        model.addElement(EEncoding.HEX);
        model.addElement(EEncoding.EBCDIC);
        jComboBox1.setModel(model);
        DefaultComboBoxModel model1 = new DefaultComboBoxModel();
        model1.addElement(EStructureBitmapFirstBitBehavior.CONTINUE);
        model1.addElement(EStructureBitmapFirstBitBehavior.FIELD);
        jComboBox2.setModel(model1);
        update();
    }

    @Override
    public void update() {
        opened = false;
        Bitmap b = field.getStructure().getBitmap();
        jComboBox1.setSelectedItem(EEncoding.getInstance(b.getEncoding()));
        jComboBox2.setSelectedItem(EStructureBitmapFirstBitBehavior.getInstance(b.getFirstBitBehavior()));
        jSpinner1.setValue(b.getBlockLength());
        opened = true;
        super.update();
    }

