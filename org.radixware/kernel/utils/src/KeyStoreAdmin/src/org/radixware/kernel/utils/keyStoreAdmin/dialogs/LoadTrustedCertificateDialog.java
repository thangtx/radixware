    protected void doClose(int retStatus) {
        returnStatus = retStatus;
        if (returnStatus==RET_OK){
            if (!checkInput())
                return;
            alias = jTextFieldAlias.getText();
            certificateFilePath = jTextFieldCertificateFile.getText();
        }

        setVisible(false);
        dispose();
    }

        /** Checks that input values are valid */
    private boolean checkInput(){
        String messageBoxTitle = bundle.getString("JOptionPaneInputError.title");
        String message;

        if (jTextFieldAlias.getText().length()==0){
            message = bundle.getString("AliasIsEmpty");
            JOptionPane.showMessageDialog(this, message, messageBoxTitle, JOptionPane.ERROR_MESSAGE);
            jTextFieldAlias.requestFocusInWindow();
            return false;
        }
        
        if (aliases.contains(jTextFieldAlias.getText())){
            message = bundle.getString("AliasExists");
            JOptionPane.showMessageDialog(this, message, messageBoxTitle, JOptionPane.ERROR_MESSAGE);
            jTextFieldAlias.requestFocusInWindow();
            return false;
        }

        if (jTextFieldCertificateFile.getText().length()==0){
            message = bundle.getString("CertificateFilePathIsEmpty");
            JOptionPane.showMessageDialog(this, message, messageBoxTitle, JOptionPane.ERROR_MESSAGE);
            jTextFieldCertificateFile.requestFocusInWindow();
            return false;
        }

        File tmpFile = new File(jTextFieldCertificateFile.getText());
        if (tmpFile.exists()){
            if (!tmpFile.isFile()){
                message = bundle.getString("NotAFileCertificate");
                JOptionPane.showMessageDialog(this, message, messageBoxTitle, JOptionPane.ERROR_MESSAGE);
                jTextFieldCertificateFile.requestFocusInWindow();
                return false;
            }
        }

        return true;
    }

    @Action
    public void selectCertificateFile(){
        String title = bundle.getString("openCertificateChooser.dialogTitle");
        KeyStoreAdminFileFilter filter = new KeyStoreAdminFileFilter(new String[]{"cer", "pem"}, "Trusted certificate (*.cer, *.pem)");
        File selectedFile = KeyStoreAdminFileSelector.selectFileForOpen(this, title, new KeyStoreAdminFileFilter[]{filter}, defaultCertificateFilePath, SelectNewFileMode.FORBIDDEN);
        if (selectedFile!=null)
            jTextFieldCertificateFile.setText(selectedFile.getAbsolutePath());
    }


