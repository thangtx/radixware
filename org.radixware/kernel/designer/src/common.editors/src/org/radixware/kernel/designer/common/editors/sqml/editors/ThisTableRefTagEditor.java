    @Override
    protected boolean tagDefined() {
        return refEditPanel.isCorrect();
    }

    @Override
    protected String getTitle() {
        return NbBundle.getMessage(ThisTableRefTagEditor.class, "this-table-ref-tag");
    }

    @Override
    protected void applyChanges() {
        refEditPanel.applyChanges();
    }

    @Override
    protected void afterOpen() {
        DdsTableDef table = null;

        if (getObject().getOwnerSqml() != null) {
            table = getObject().getOwnerSqml().getEnvironment().findThisTable();
        }

        if (table == null) {
            table = getOpenInfo().getLookup().lookup(DdsTableDef.class);
        }

        refEditPanel.open(getObject(), new EditorOpenInfo(true, Lookups.fixed(table)));
    }

    @Override
    protected void setReadOnly(boolean readOnly) {
        refEditPanel.setReadOnly(readOnly);
    }
