    public void addActionListener(final ActionListener l) {
        charPanel1.addActionListener(this);
        setParentPanel1.addActionListener(this);
        al = l;
    }

    public void removeActionListener(final ActionListener l) {
        charPanel1.removeActionListener(this);
        setParentPanel1.removeActionListener(this);
        al = null;
    }

    public void setString(String value, String parentValue) {
        Character val = null;
        if (value != null && !value.equals(""))
            val = value.charAt(0);
        Character pval = null;
        if (parentValue != null && !parentValue.equals(""))
            pval = parentValue.charAt(0);
        setCharacter(val,pval);
    }

    public String getString() {
        Character ch = getCharacter();
        if (ch == null)
            return null;
        else
            return ch.toString();
    }

    public void setCharacter(Character value, Character parentValue) {
        this.parentValue = parentValue;
        this.currentValue = value;
        charPanel1.removeActionListener(this);
        charPanel1.setChar(value);
        setParentPanel1.setSelected(value == null && parentValue != null);
        setParentPanel1.setEnabled(parentValue != null);
        if (value == null)
            charPanel1.setChar(parentValue);
        charPanel1.setEnabled(value!=null||parentValue==null);
        charPanel1.addActionListener(this);
    }

    public Character getCharacter() {
        if (setParentPanel1.getSelected())
            return null;
        else
            return charPanel1.getChar();
    }


