    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getSource() instanceof JCheckBox) {
            JCheckBox cb = (JCheckBox)e.getSource();
            charPanel1.removeActionListener(this);
            if (cb.isSelected()) {
                charPanel1.setChar(parentValue);
                charPanel1.setEnabled(false);
            }
            else {
                charPanel1.setChar(null);
                charPanel1.setEnabled(true);
            }
            charPanel1.addActionListener(this);
        }
        currentValue = charPanel1.getChar();
        al.actionPerformed(new ActionEvent(this,1,""));
    }

