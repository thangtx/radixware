    static Map<EIsoLanguage, String> comments = new HashMap<EIsoLanguage, String>();

    private static class Element extends ChangeListEditorPanel.MessageElement {

        private String name;

        Element(String name) {
            this.name = name;
        }

        @Override
        public String getDisplayName() {
            return name;
        }

        @Override
        protected List<Layer> getLayers() {
            return null;
        }
    }

    /** Creates new form TestForm */
    public TestForm() {
        initComponents();
        setLayout(new BorderLayout());
        ChangeListEditorPanel panel = new ChangeListEditorPanel();
        getContentPane().add(panel);
        SortedMap<Layer, ChangeList> map = new TreeMap<Layer, ChangeList>();


        List<ChangeListEditorPanel.MessageElement> elements = new ArrayList<ChangeListEditorPanel.MessageElement>();

        Element e = new Element("one");
        e.setMessage(EIsoLanguage.ENGLISH, "English1");
        e.setMessage(EIsoLanguage.RUSSIAN, "Russion1");
        elements.add(e);
        e = new Element("two");
        e.setMessage(EIsoLanguage.ENGLISH, "English2");
        e.setMessage(EIsoLanguage.RUSSIAN, "Russion2");
        elements.add(e);
        e = new Element("three");
        e.setMessage(EIsoLanguage.ENGLISH, "English3");
        e.setMessage(EIsoLanguage.RUSSIAN, "Russion3");
        e.setMessage(EIsoLanguage.AFAR, "Afar3");
        elements.add(e);

        panel.open(map, elements, EnumSet.of(EIsoLanguage.ENGLISH, EIsoLanguage.RUSSIAN, EIsoLanguage.UKRAINIAN));
        this.addWindowListener(new WindowListener() {

            @Override
            public void windowOpened(WindowEvent e) {
            }

            @Override
            public void windowClosing(WindowEvent e) {
                for (EIsoLanguage lang : comments.keySet()) {
                    System.out.println(comments.get(lang));
                }
            }

            @Override
            public void windowClosed(WindowEvent e) {
                for (EIsoLanguage lang : comments.keySet()) {
                    System.out.println(comments.get(lang));
                }
            }

            @Override
            public void windowIconified(WindowEvent e) {
            }

            @Override
            public void windowDeiconified(WindowEvent e) {
            }

            @Override
            public void windowActivated(WindowEvent e) {
            }

            @Override
            public void windowDeactivated(WindowEvent e) {
            }
        });
    }

