    protected void doClose(int retStatus) {
        returnStatus = retStatus;
        if (returnStatus==RET_OK){
            if (!checkInput())
                return;
            alias = (String)jComboBoxAlias.getSelectedItem();
            oldPassword = jPasswordFieldOld.getPassword();
            newPassword = jPasswordField.getPassword();
        }

        setVisible(false);
        dispose();
    }

    /** Checks that input values are valid */
    private boolean checkInput(){
        String messageBoxTitle = bundle.getString("JOptionPaneInputError.title");
        String message;

        if (jPasswordFieldOld.getPassword().length==0){
            message = bundle.getString("OldPasswordIsEmpty");
            JOptionPane.showMessageDialog(this, message, messageBoxTitle, JOptionPane.ERROR_MESSAGE);
            jPasswordFieldOld.requestFocusInWindow();
            return false;
        }

        if (jPasswordField.getPassword().length==0){
            message = bundle.getString("NewPasswordIsEmpty");
            JOptionPane.showMessageDialog(this, message, messageBoxTitle, JOptionPane.ERROR_MESSAGE);
            jPasswordField.requestFocusInWindow();
            return false;
        }

        if (!Arrays.equals(jPasswordField.getPassword(), jPasswordFieldConfirmation.getPassword())){
            String dialogMessage = bundle.getString("WrongNewPasswordConfirmation");
            JOptionPane.showMessageDialog(this, dialogMessage, messageBoxTitle, JOptionPane.ERROR_MESSAGE);
            jPasswordFieldConfirmation.requestFocusInWindow();
            return false;
        }

        return true;
    }


