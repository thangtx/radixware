    private static final int DX = 20;
    private static final int DY = 65;

    @Override
    public Dimension getPreferredSize() {
        Dimension size = super.getPreferredSize();
        for (EditorDialog.EditorPanel panel: panels) {
            Dimension sz = panel.getPreferredSize();
            size.width = Math.max(size.width, sz.width + DX);
            size.height = Math.max(size.height, sz.height + DY);
        }
        return size;
    }

    @Override
    public Dimension getMinimumSize() {
        Dimension size = super.getMinimumSize();
        for (EditorDialog.EditorPanel panel: panels) {
            Dimension sz = panel.getMinimumSize();
            size.width = Math.max(size.width, sz.width + DX);
            size.height = Math.max(size.height, sz.height + DY);
        }
        return size;
    }

    @Override
    public void init() {
        textName.setText(obj.getName());
        for (EditorDialog.EditorPanel panel: panels) {
            panel.init();
            panel.activate();
        }

        //int sel = panelTab.getSelectedIndex();
        //if (sel >=0 && sel <= panels.size()-1)
        //    panels.get(sel).activate();

        panelTab.addChangeListener(new ChangeListener() {
            @Override
            public void stateChanged(ChangeEvent evt) {
                int sel = panelTab.getSelectedIndex();
                if (sel >=0 && sel <= panels.size()-1)
                    panels.get(sel).activate();
            }
        });

        textName.requestFocusInWindow();
    }

    @Override
    public void apply() {
        for (EditorDialog.EditorPanel panel: panels)
            panel.apply();
    }

    @Override
    public String getTitle() {
        String title = NbBundle.getMessage(getClass(), "CTL_Properties");
        Item item = Item.getItem(obj);
        if (item != null)
            title = item.getTitle() + " " + title;
        return title;
    }
