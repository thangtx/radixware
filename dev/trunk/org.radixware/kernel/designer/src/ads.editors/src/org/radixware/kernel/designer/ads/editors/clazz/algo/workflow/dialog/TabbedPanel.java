    public TabbedPanel(final AdsAppObject node, List<EditorDialog.EditorPanel> panels) {
        super(node);
        initComponents();

        int idx = 0;
        for (EditorDialog.EditorPanel panel: panels) {
            panelTab.add(panel.getTitle(), panel);
            panelTab.setIconAt(idx++, panel.getIcon() != null ? panel.getIcon().getIcon() : null);
            panel.setEnabled(!obj.isReadOnly());
        }

        textName.getDocument().addDocumentListener(new DocumentListener() {

            @Override
            public void insertUpdate(DocumentEvent e) {
                obj.setName(textName.getText());
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                obj.setName(textName.getText());
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                obj.setName(textName.getText());
            }
        });
        
        textName.setEnabled(!node.isReadOnly());
        this.panels = panels;
    }

