    private AdsClassCatalogDef classCatalog;
    public void open(AdsClassCatalogDef classCatalog){
        this.classCatalog = classCatalog;
        update();
    }

    public void update(){
        if (classCatalog != null){
            AdsClassCatalogDef overriden = classCatalog.getHierarchy().findOverridden().get();
            if (overriden != null){
                editor.setVisible(true);
                jLabel1.setVisible(true);
                gap.setVisible(true);
                editor.open(overriden, overriden.getId());
            } else {
                editor.setVisible(false);
                jLabel1.setVisible(false);
                gap.setVisible(false);
                editor.open(null, null);
            }
            accessPanel.open(classCatalog); 
        }
    }

