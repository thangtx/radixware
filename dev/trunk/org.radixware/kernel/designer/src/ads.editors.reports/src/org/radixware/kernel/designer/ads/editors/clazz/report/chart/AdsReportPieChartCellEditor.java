    /**
     * Creates new form AdsReportPieChartPanel
     */
    public AdsReportPieChartCellEditor() {
        initComponents();
        pieChartDatasetPanel = new DatasetPieChartPanel();
        createUi();
    }

    private final void createUi() {
        final javax.swing.JPanel content = new javax.swing.JPanel();
        final GridBagLayout gbl = new GridBagLayout();
        final GridBagConstraints c = new GridBagConstraints();
        content.setLayout(gbl);
        c.insets = new Insets(0, 2, 10, 2);
        c.fill = GridBagConstraints.HORIZONTAL;
        c.gridwidth = 2;
        c.weightx = 1.0;
        gbl.setConstraints(seriesTitle, c);
        content.add(seriesTitle);

        c.gridx = 0;
        c.gridy = 1;
        c.gridwidth = 2;
        c.insets = new Insets(0, 0, 10, 0);
        c.weightx = 0.0;
        gbl.setConstraints(pieChartDatasetPanel, c);
        content.add(pieChartDatasetPanel);

        setLayout(new BorderLayout());
        add(content, BorderLayout.NORTH);
        setPanelEnabled(false);
    }

    @Override
    public void open(final AdsReportChartCell cell, final AdsReportClassDef report, final Map<Id, IMultilingualStringDef> mlStrings) {
        super.open(cell, report, mlStrings);
        pieChartDatasetPanel.open(report);
    }

    @Override
    protected void selectSeries(final AdsReportChartSeries series) {
        super.selectSeries(series);
        if (series == null) {
            clearPanel();           
        } else {
            pieChartDatasetPanel.selectSeries(series);
        }
    }

    @Override
    protected void clearPanel() {
        super.clearPanel();
        pieChartDatasetPanel.clearPanel();
    }

    @Override
    protected void setPanelEnabled(final boolean enable) {
        super.setPanelEnabled(enable);
        pieChartDatasetPanel.setPanelEnabled(enable);
    }

